# En python existen 4 tipos de datos

# 1. String sirve para guardar cadenas de texto
nombre: str = "Daniel"
# 2. Integer sirve para guardar numeros enteros
edad: int = 18
# 3. Float sirve para guardar numeros decimales
nota: float = 100.0 
# 4. Boolean sirve para guardar valores logicos si son verdaderos o falsos
aprobado: bool = True
desaprobado: bool = False


